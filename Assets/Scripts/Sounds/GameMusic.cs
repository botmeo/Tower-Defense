using UnityEngine;

public class GameMusic : MonoBehaviour
{
    private static GameMusic instance;
    public static GameMusic Instance { get => instance; }

    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
